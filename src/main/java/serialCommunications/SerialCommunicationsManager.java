package serialCommunications;

import io.dropwizard.lifecycle.Managed;

/**
 * Class adds Managed Object (lifecycle) capabilities to the SerialCommunications class.
 * Allows the runtime environment to control SerialCommunications, specifically the closing of the 
 * serial port, and to cancel existing heartbeat timers.
 */
public class SerialCommunicationsManager implements Managed
{
	private final SerialCommunications communications;
	
	public SerialCommunicationsManager(SerialCommunications serial)
	{
		this.communications = serial;
	}
	
	public void start()
	{
		//do nothing
	}
	
	public void stop()
	{
		//close the serial port
		communications.close();
		//cancel heartbeat timer
		communications.cancelTimer();
	}
}

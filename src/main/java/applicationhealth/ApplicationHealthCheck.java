package applicationhealth;

import com.codahale.metrics.health.HealthCheck;


/**
 * A trivial application health check
 * Application will be monitored by an external service
 */
public class ApplicationHealthCheck extends HealthCheck
{
	private final String healthCheck;

    public ApplicationHealthCheck(String healthCheck) 
    {
        this.healthCheck = healthCheck;
    }

    @Override
    protected Result check() throws Exception 
    {
    	//the string testHealth should read 'healthy Application' if the app is working
    	final String testHealth = String.format(healthCheck, "Application");
        if (!testHealth.contains("Application")) 
        {
            return Result.unhealthy("Healthcheck Failed");
        }
        return Result.healthy();
    }
}

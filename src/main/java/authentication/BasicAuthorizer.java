package authentication;

import org.slf4j.Logger;
import org.slf4j.LoggerFactory;

import entities.User;
import io.dropwizard.auth.Authorizer;


/**
 * Class is used to authorize a User for a given resource.
 */
public class BasicAuthorizer implements Authorizer<User>
{
	private static final Logger LOGGER = LoggerFactory.getLogger(BasicAuthorizer.class);
	
	
	public boolean authorize(User user, String role) 
	{
		LOGGER.trace("BasicAuthorizer method authorize called with user: {}, and role: {}.", user, role);
		
		return user.getRole() != null && user.getRole().contains(role);
    }
}